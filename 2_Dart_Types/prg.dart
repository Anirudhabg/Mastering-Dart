void main(){
  int a = 10;
  double b = 20.5;
  String c = "Hello, Dart!";
  bool d = true;

  print("Integer: $a");
  print("Double: $b");
  print("String: $c");
  print("Boolean: $d");

  var e = "I am a variable";
  print(e);

  var numbers = [1, 2, 3, 4, 5];
  print("List: $numbers");

  var info = {'name': 'Dart', 'type': 'Programming Language'};
  print("Map: $info");

  var n = null;
  print("Null value: $n");

  var setOfNumbers = {1, 2, 3, 4, 5};
  print("Set: $setOfNumbers");
}